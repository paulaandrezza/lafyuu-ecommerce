fun getEnvVar(key: String): String? {
    val envFile = rootProject.file(".env")
    if (envFile.exists()) {
        val props = Properties()
        envFile.inputStream().use { props.load(it) }
        return props.getProperty(key)
    }
    return System.getenv(key)
}

